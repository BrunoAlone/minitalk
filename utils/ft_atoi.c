int	ft_atoi(const char *n)
{
	int	i;
	int	sinal;
	int	result;

	i = 0;
	sinal = 1;
	result = 0;
	while ((n[i] >= 7 && n[i] <= 13) || n[i] == 32)
		i++;
	if (n[i] == '-' || n[i] == '+')
	{
		if (n[i] == '-')
			sinal = -1;
		i++;
	}
	while (n[i] >= '0' && n[i] <= '9')
	{
		result = (result * 10) + (n[i] - '0');
		i++;
	}
	return (result * sinal);
}
